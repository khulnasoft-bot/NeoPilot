//! A window, which is a top-level container for UI elements.
//!
//! A window is a rectangular region on the user's screen that can contain other
//! UI elements. It can be interacted with using a mouse and keyboard.
//!
//! The window is created using the `WindowBuilder` type. The window builder
//! provides a set of methods to configure the window, such as setting its size,
//! title, and window decorations.
//!
//! Once the window is created, it can be used to create other UI elements such as
//! buttons and text boxes.
